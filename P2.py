def ip_en_rango(ip):
    try:
        ip_obj = ipaddress.IPv4Address(ip)
        # Solo permite IPs desde 1.0.0.0 hasta 223.255.255.255 (clase A, B, C)
        return ip_obj >= ipaddress.IPv4Address("1.0.0.0") and ip_obj <= ipaddress.IPv4Address("223.255.255.255")
    except ValueError:
        return False
